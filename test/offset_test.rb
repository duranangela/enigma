require 'minitest/autorun'
require 'minitest/pride'
require './lib/offset'

class OffsetTest < Minitest::Test

  def test_it_returns_correct_date_code
    offset = Offset.new.offset('020315')
    assert_equal '9225', offset
  end

  def test_it_can_work_with_a_different_date
    offset = Offset.new.offset('123178')
    assert_equal '9684', offset
  end

end
